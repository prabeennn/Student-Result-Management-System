    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll=jTextField1.getText();
        String java=jTextField2.getText();
        String javaScript=jTextField3.getText();
        String dbms=jTextField4.getText();
        String python=jTextField5.getText();
        String mern=jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","1234");
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,java,javascript,dbms,python,mern) values('"+roll+"','"+java+"','"+javaScript+"','"+dbms+"','"+python+"','"+mern+"')");
                JOptionPane.showMessageDialog(null, "Marks saved successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "This roll number is registered");
            
        }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

